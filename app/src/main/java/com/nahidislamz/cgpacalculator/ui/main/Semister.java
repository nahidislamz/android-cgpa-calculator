package com.nahidislamz.cgpacalculator.ui.main;

public class Semister {
    float credits,sGPA,totalCredits,totalSGPA;

    public Semister() {
    }

    public float getCredits() {
        return credits;
    }

    public void setCredits(float credits) {
        this.credits = credits;
    }

    public float getsGPA() {
        return sGPA;
    }

    public void setsGPA(float sGPA) {
        this.sGPA = sGPA;
    }

    public float getTotalCredits() {
        return totalCredits;
    }

    public void setTotalCredits(float totalCredits) {
        this.totalCredits = totalCredits;
    }

    public float getTotalSGPA() {
        return totalSGPA;
    }

    public void setTotalSGPA(float totalSGPA) {
        this.totalSGPA = totalSGPA;
    }
}
